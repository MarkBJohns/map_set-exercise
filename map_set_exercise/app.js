function vowelCount(str){
    const vowelMap=new Map();
    const vowels=['a','e','i','o','u'];
    for(const char of str.toLowerCase()){
        if(vowels.includes(char))vowelMap.set(char,(vowelMap.get(char)||0)+1);
    }
    return vowelMap;
}